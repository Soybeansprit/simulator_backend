package com.example.demo.bean;

public class AttributeValue {
	///////场景树中各可设置属性赋值
	private String name="";
	private double value=0;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
}
